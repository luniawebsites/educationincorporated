                    <span class="article__author-name">By Jacqueline Aitchison</span>

					<p>A small independent school like EduInc is ideally placed to be nimble and pivot quickly. And that is exactly what we did in response to the pandemic.</p>

                    <h3>Our Response</h3>
					<p> We paid attention to what was happening in the world and started our COVID-19 planning early with a questionnaire sent to our parent body. The objective was to establish 3 things:</p>
                    <p>
                        <ul class="list-check">
                            <li>What devices students had sole access to at home (not shared with siblings or family members).</li>
                            <li>Their level of internet connectivity.</li>
                            <li>Their power backup facilities at home.</li>
                        </ul>
                    </p>
                    <p>The answers to these questions informed which online platforms and applications were most suitable for our online teaching plans, who we (the school) would need to assist with devices and what plan would be put in place if power interruptions were added to the mix. It similarly informed our need to arrange for additional devices to be sent home where required.</p>
                    <p>The second step was a "soft test" where all the students would bring their devices to school for a day and we would run the timetable as normal. This would allow us to iron out as many glitches as possible while still in the same physical space. The third step was to run a "hard test" where the students would spend a day at home, again running a normal timetable and ironing out final hiccups.</p>
                    <p>In the first week of March we presented the full COVID-19 contingency plan to our Board and admittedly, even at that point, we did not really believe that a country-wide school shutdown was imminent. It felt alarmist and was difficult to visualise. On Sunday 15 March, however, our President announced that schools were to be closed by Thursday 19 March and our plan went into overdrive. On Monday 16 March we released our first COVID-19 podcast to our parents and students detailing what the coming days would look like and what was required of them. On Tuesday 17 March we did our soft test at school and on Wednesday 18 March we ran our first day of online schooling from home with 100% attendance and 100% teaching taking place. Not homeschooling. Not self-study. Rather, schooling at home following our regular timetable with teachers present on various platforms in every lesson, visible to the students.</p>
                    <p>We finished our first term on 9 April without missing a single hour of "real" teaching.</p>
                    <p>We are very proud of that.</p>
                    <p>Was it easy? Of course not. The teachers, as a team, upskilled themselves on online teaching platforms at a rate Silicon Valley IPOs would find impressive and continued to support each other with training, practice and sharing tips in the afternoons after classes, while also adapting their class prep for the new platforms. The students, as a school, embraced the new way of learning to the point that many would be online by 6:45 am asking if they could start their 7:30 am lesson early. We may all have been at home but the energy flowing between us all, on the chats, Meets, Hangouts, etc. was electric with agility clearly evident.</p>
                    <p>Importantly, we did not scale down the curriculum. We scaled it up. We added daily reading tasks to be recorded and uploaded, touch typing and Udemy online courses for all students in grades 4 to 9. When we closed on 9 April we made it clear that four weeks off with no stimulation was not an option, so teachers have continued with a multitude of online clubs from music to investment clubs, a COVID-19 time capsule project and numerous one-on-one interventions with struggling students. </p>
                    <p>There are many reasons why the last month of education in the EduInc community has brought me hope. Notably, the positivity, support and encouragement from our parent body has been the highest ever, and we are so grateful for that. The performance of the teachers has been consistently commendable and the enthusiasm, engagement and students' support of each other has been unprecedented. This is what gives me hope for a new face of education as we navigate EduInc into these unchartered waters.</p>

