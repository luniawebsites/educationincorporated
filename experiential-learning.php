					<p>How we learn, really learn, is so much more than simply memorising content and regurgitating it when tested. Real learning is a complex relationship between a student and the content they are learning. It involves critical interaction and engagement, application, evaluation and iteration. This relationship understands that challenge precedes learning, and that trial and error, and sometimes even failing, are essential components of successful learning.</p>

					<p>Experiential learning is important to us at Education Incorporated because it allows learning to take place in a manner that equips our students for life-long learning. It teaches our students to test their own knowledge, how to reflect on their learning, experiment and apply feedback, to communicate effectively and efficiently and, ultimately, develop the ability for abstract conceptualisation.</p>

					<p>We believe that the best way to do this is first-hand. We may be small, but this benefits our learners when it comes to opening doors that would be closed to bigger school groups. Our students have been to Google, Twitter and Facebook Head Quarters in Dublin, Ireland, to learn about Life Design, talk to people who work there and learn how to become a part of such organisations. We have spent time at the Anglo American CEL learning System Optimisations. We have learned mindfulness from Gelong Thubten, "the Google monk" himself. Our leaders learn youth leadership directly from the Rotary Club, learning servant leadership amongst their peers. These are experiences that set our students apart and catapult them into 21st century learning, really equipping them for life after school.</p>

					<p>Experiential learning is the process of learning through experience, and is more specifically defined as "learning through reflection on doing".</p>

