					<h3>Give your child the best possible chance to perform</h3>

					<p>This mismatch is often a result of an incongruence between the child and his/her school environment. A growing number of children are simply not comfortable in the traditional class of 25 to 30+ pupils, but are also not remedial candidates. They are children who respond to a more interactive and individualised approach than a large class can provide, and are academically very capable. All they need to succeed academically is an environment that recognises them as individuals and who can respond to that need. </p>

					<p><strong>Here's Charlie's story which may help you too:</strong></p>

					<p>Charlie attended a private school from grade R and by grade 5 had repeated a year, was regularly seeing an occupational therapist, a speech therapist and a psychologist. As if this weren't enough, he also had an assistant throughout his school day to help him read, organise his books, stationery, classwork and schedule and help him with homework. Despite all these measures, he was still performing poorly and in danger of failing another year.</p>

					<p>We advised Charlie's parents that the constant assistance, repeated psycho-educational testing and arguably intense therapies had reached a point of reinforcing only one thing in Charlie: that there was something wrong with him. His parents were frustrated at spending so much money every month for what seemed like very little progress. Charlie was perhaps even more frustrated as he had lost all sense of independence and control over his school work and educational environment.</p>

					<p>Charlie came to Education Incorporated at the beginning of his grade 6 year. His therapy schedule was reduced to occasional visits to the psychologist and he no longer had an assistant in class with him. The first two terms were tough for Charlie. He had to learn to start thinking for himself, to take ownership of work and to experience the consequences of his decisions, both good and unpleasant. He was taught how to study effectively and how to manage his time and organise his work space. He was expected to make an effort and work. Within two terms Charlie's marks had improved dramatically (including a couple of distinctions) but most importantly, he was happy and enjoying school, feeling in control and completely able to cope with his academic schedule and requirements. Getting his academics under control meant that he could focus more time on his passion - cricket - earning himself cricket colours by the end of grade 7. His parents kept commenting on how family members and friends could not believe this was the same child.</p>

					<p>The ability to streamline Charlie's academics and allow him to excel in his chosen sport is another by-product of the very small classes and how they allow us to customise a child's educational experience around their talents and passions, allowing them the opportunity to turn these into professional careers. Education Incorporated has students who have already excelled in their sporting disciplines to provincial and national levels, even representing South Africa as Junior Springboks.</p>

					<p>These students are taught to understand the value and significance of accountability - the school assists them in structuring their academic schedules around their tournaments, travelling and gruelling training schedules, but expects commitment and dedication to their academic performance in return for this flexibility. This ability to find balance and a manner in which every person can customise their purpose in life is a vital 22nd century skill.</p>

