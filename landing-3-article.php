					<p>Currently, most schools and education systems continue with a Dickensian style&mdash;based on the 19th and 20th century industrial worker supply and demand cycle&mdash;which is no longer suited to the children of today simply because it is not geared to prepare them for tomorrow. When we say tomorrow, we are referring to the ICT age (information, communication and technology): a fast-developing phase that most players in the education system simply cannot conceptualise, nor are they prepared for it. Finland and Singapore are part of a handful of countries that have realised this challenge and are actively rethinking their existing education systems. Much of the rest of the world seems to be lagging, however, and South Africa is no exception. No longer can we hide behind statements like, "It could never work" and "It has always been done this way." The evidence is clear&mdash;our methods are failing, and we need to evolve.</p>

